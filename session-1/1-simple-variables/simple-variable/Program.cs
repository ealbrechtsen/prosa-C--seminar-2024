namespace simple_variable;

class Program
{
    static void Main(string[] args)
    {
        // erklære variabel og tildel værdi
        int variable = 10;
        // anvende operatorer
        variable++;
        Console.WriteLine(variable);

        variable--;
        Console.WriteLine(variable);

        variable += 20;
        Console.WriteLine(variable);

        // erklære double decimal
        double decimated = 12.5;

        Console.WriteLine(decimated);

        decimated++;
        Console.WriteLine(decimated);

        decimated--;
        Console.WriteLine(decimated);

        decimated *= 2;
        Console.WriteLine(decimated);
        

    }
}
